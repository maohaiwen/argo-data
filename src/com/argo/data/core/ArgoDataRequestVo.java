package com.argo.data.core;

public class ArgoDataRequestVo {

	private String platformNumber;
	private String dateFrom;
	private String dateTo;
	private String minLat;
	private String maxLat;
	private String minLong;
	private String maxLong;
	public String getPlatformNumber() {
		return platformNumber;
	}
	public void setPlatformNumber(String platformNumber) {
		this.platformNumber = platformNumber;
	}
	public String getDateFrom() {
		return dateFrom;
	}
	public void setDateFrom(String dateFrom) {
		this.dateFrom = dateFrom;
	}
	public String getDateTo() {
		return dateTo;
	}
	public void setDateTo(String dateTo) {
		this.dateTo = dateTo;
	}
	public String getMinLat() {
		return minLat;
	}
	public void setMinLat(String minLat) {
		this.minLat = minLat;
	}
	public String getMaxLat() {
		return maxLat;
	}
	public void setMaxLat(String maxLat) {
		this.maxLat = maxLat;
	}
	public String getMinLong() {
		return minLong;
	}
	public void setMinLong(String minLong) {
		this.minLong = minLong;
	}
	public String getMaxLong() {
		return maxLong;
	}
	public void setMaxLong(String maxLong) {
		this.maxLong = maxLong;
	}
	
	
}
